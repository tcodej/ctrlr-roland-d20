    console("Reset tone")
    common = {0xF0,0x41,0x10,0x16,0x12,0x04,0x00,0x00,0x44,0x65,0x66,0x61,0x75,0x6C,0x74,0x20,0x20,0x20,0x00,0x00,0x0F,0x00}
